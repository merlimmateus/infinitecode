
# Criando uma lista

nums = [1, 2, 3]

# Adicionando elemento ao final da lista
nums.append(4)

# Inserir em um índice específico
nums.insert(2, 5)

# Remover último elemento ( ou um índice específico )
nums.pop()
nums.pop(2)

# Ordernar lista
nums.sort()

# Remove o primeiro valor que achar dentro da lista através do valor passado pelo parâmetro
nums.remove(1)

# Ordenar lista de forma reversa
nums.sort(reverse=True)

# Reverte a lista
nums.reverse()
